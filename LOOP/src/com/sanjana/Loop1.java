package com.sanjana;
public class Loop1 {
    public static void main(String[] args) {
        System.out.println("Welcome to loops");
        System.out.println("For loop");
        int iCnt = 0;
        //for loop
        for(iCnt = 0; iCnt <= 10; iCnt++) {
            System.out.println(iCnt);
        }

    }

}