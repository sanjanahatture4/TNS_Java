package com.sanjana;

public class Operator2 {

    public static void main(String[] args)
    {
        //bitwise operators
        //(&,|,^,~)
        int a=5;
        int b=3;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        //unary operators
        //(++,--)
        //pre increment
        System.out.println(++a);
        System.out.println(++b);
        //post increment
        System.out.println(a++);
        System.out.println(b++);
        //pre decrement
        System.out.println(--a);
        System.out.println(--b);
        //post decrement
        System.out.println(a--);
        System.out.println(b--);

    }
}
