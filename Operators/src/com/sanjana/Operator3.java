package com.sanjana;

public class Operator3 {

        public static void main(String[] args) {
            int a=10;
            int b=12;
            //assignment operators
            System.out.println(a+=b);//it will change the value of a and the same value will alloted to next term
            System.out.println(a-=b);//this a value will alloted to next term
            System.out.println(a*=b);
            System.out.println(a/=b);
            //shift operators in java
            System.out.println(a<<2);//left shift
            System.out.println(b>>2);//right shift operator

        }
}
